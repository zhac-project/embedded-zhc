// Tier 2: Ubisys J1 — uses shared ubisys converters.
// Shutter / blind controller. Real device exposes a tilt-or-lift
// surface that varies with `windowCoveringType` (attr 0x0000 of
// 0x0102) — z2m switches between position-only / tilt-only / both.
// Without runtime device-attribute access we expose both axes; the
// extra one is silently ignored on lift-only or tilt-only types.
//
// Parity fix (real gap): the def declared only `position` and wired the
// generic `kFzCoverPosition`, which decodes closuresWindowCovering attr
// 0x0008 (lift) only. z2m's `fz.cover_position_tilt` also decodes attr
// 0x0009 (CurrentPositionTiltPercentage → `tilt`), which the J1 reports.
// `tilt` was therefore both missing as an expose and undecoded. Now
// declares `tilt` and wires `kFzUbisysCoverPositionTilt` (lift+tilt,
// 0xFF "unknown" sentinel skipped).
// z2m-source: ubisys.ts #J1.
