// Tier 2: Danfoss Icon2 — multi-room TRV, up to 16 rooms.
// Endpoint labels l1..l15 → endpoints 1..15; l16 → endpoint 232 (firmware
// quirk: the 16th "room" is a system-wide endpoint, not 16). Per-room
// thermostat exposes (`local_temperature_l1`, `current_heating_setpoint_l1`,
// `system_mode_l1`, ...) flow through the runtime's multi-endpoint suffix
// path (landed 2026-04-28k):
//   * Inbound: `dispatch_from_zigbee` rewrites every emitted Fz key with
//     `_<label>` based on the source endpoint (e.g. EP 3 → `state_l3`)
//     for keys not in the global blocklist (battery / linkquality / etc.).
//   * Outbound: `zhac_adapter::dispatch_and_send` strips `_<label>`
//     from the SPA-supplied key and routes the frame to the matching
//     endpoint instead of EP 1.
//
// Icon2 Main Controller (0x0210/0x0211), Room Thermostat (0x8040/0x8041),
// Sensor (0x0042). The 13 Icon-only manuSpec attrs (floor_sensor_mode,
// floor_min/max setpoint, schedule_type_used, icon2_pre_heat[_status],
// system_status_code, heat_supply_request, multimaster_role,
// icon_application, icon_forced_heating_cooling) + `haDiagnostic`
// (0x0B05) cluster still need a `danfoss_icon2/_shared` runtime — left
// as TODO. Per-room standard hvacThermostat works today.
//
// z2m-source: danfoss.ts #Icon2 (lines 1346-1365 declare the same
// l1..l15 + l16=232 endpoint map).
